    private void themPM() {
        
         String mapm = null;
        int a;
        AD:
        for (int i = 1; i < 1000; i++) {
            a = i;
            String b = Integer.toString(a);
            if (b.length() == 1) {
                mapm = "PM00" + a;
            } else if (b.length() == 2) {
                mapm = "PM0" + a;
            } else if (b.length() > 2) {
                mapm = "PM" + a;
            } else {
            }
            boolean khIsExixst = false;
            for (Phieumuon pm : list) {
                if (pm.getMaphieumuon().equals(mapm)) {
                    khIsExixst = true;
                    break;
                }
            }
            if (!khIsExixst) {
                break;
            }
        }

       String madg = txtPMMADOCGIA.getText();
       String manv = txtPMMANV.getText();
       String ngaym = txtPMNGAYMUON.getText();
       String ngayt = txtPMNGAYTRA.getText();
       int tt ;
       if(ckbTTDATRA.isSelected()){
       tt = 0;
       }else{
       tt = 1;
       }
       Phieumuon pm = new Phieumuon(mapm, madg, manv, ngaym, ngayt, tt);
       PhieumuonDao.themPhieumuon(pm);
       JOptionPane.showMessageDialog(this, "Thêm thành công");
    }


    private void laydulieuPhieumuon(String sql) {
        try {
            list.clear();
            tblPHIEUMUON.removeAll();
            list = PhieumuonDao.laydulieuPhieumuon(sql);
            fillToTablePhieumuon();
            laydulieuPM();
        } catch (SQLException ex) {
            Logger.getLogger(frmPHIEUMUON.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void fillToTablePhieumuon() {
        DefaultTableModel model = (DefaultTableModel) tblPHIEUMUON.getModel();
        model.setRowCount(0);
        for (Phieumuon pm : list) {
            Object[] row = new Object[]{pm.getMaphieumuon(), pm.getMadg(), pm.getManv(), pm.getNgaymuon(), pm.getNgaytra(), pm.getTrangthai()};
            model.addRow(row);
        }
    }

    private void laydulieuPM() {
        try {
            this.currentindex = 0;
            if (list.size() == 1) {
                this.currentindex = 0;
            }
            Phieumuon pm = list.get(currentindex);
            txtMAPM.setText(pm.getMaphieumuon());
            txtPMMANV.setText(pm.getManv());
            txtPMMADOCGIA.setText(pm.getMadg());
            txtPMNGAYMUON.setText(pm.getNgaymuon());
            txtPMNGAYTRA.setText(pm.getNgaytra());
            if (pm.getTrangthai() == 0) {
                ckbTTDATRA.setSelected(true);
            } else {
                ckbTTCHUATRA.setSelected(true);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Dữ liệu trống");
        }
    }


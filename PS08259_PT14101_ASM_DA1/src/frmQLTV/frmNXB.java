    private void themNXB() {
        String manxb = null;
        int a;
        AD:
        for (int i = 1; i < 1000; i++) {
            a = i;
            String b = Integer.toString(a);
            if (b.length() == 1) {
                manxb = "NXB00" + a;
            } else if (b.length() == 2) {
                manxb = "NXB0" + a;
            } else if (b.length() > 2) {
                manxb = "NXB" + a;
            } else {
            }
            boolean khIsExixst = false;
            for (NhaXB nxb : list) {
                if (nxb.getManxb().equals(manxb)) {
                    khIsExixst = true;
                    break;
                }
            }
            if (!khIsExixst) {
                break;
            }
        }
        String ten = txtTENNXB.getText();
        String diachi = txtNXBDIACHI.getText();
        String email = txtNXBEMAIL.getText();
        NhaXB nxb = new NhaXB(manxb, ten, diachi, email);
        NhaXBDao.themNhaXB(nxb);
        JOptionPane.showMessageDialog(this, "Thêm NXB thành công");
    }


    private void laydulieuNXB(String sql) {
        try {
            tblNXB.removeAll();
            list.clear();
            list = NhaXBDao.laydulieuNhaXB(sql);
            fillToTableNXB();
            laydulieuNXB1();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Tải dữ liệu lên thất bại");
        }
    }

    private void laydulieuNXB1() {
        try {
            if (list.size() == 1) {
                this.currentindex = 0;
            }
            NhaXB nxb = list.get(currentindex);
            txtMANXB.setText(nxb.getManxb());
            txtTENNXB.setText(nxb.getTennxb());
            txtNXBDIACHI.setText(nxb.getDiachi());
            txtNXBEMAIL.setText(nxb.getEmail());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Dữ liệu trống");
        }
    }

